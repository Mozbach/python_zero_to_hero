# Check for equality using ==
print(2 == 2) # evaluates to True
print(2 == 1) # evaluates to False

# Unlike JS, Python will consider types:
print("2" == 2) # evaluates to False

# And python is very case sensitive
print("B" == "b") # evaluates to False

# Check if something is NOT the same as something else
print(2 != 1) # evaluates to True
print(2 != 2) # evaluates to False

# Greater than > : If the value on the left is greater than the value on the right
# Less than < : If the value on the left is less than the value on the right
# Greater Than or Equals To: >=
# Less than or equals to:  <=